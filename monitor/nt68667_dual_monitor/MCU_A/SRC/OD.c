void EnableOD(void)
{
	WriteIIC560(0x1ee,0x00);  // evan data 071211	
	WriteIIC560(0x4D4,0x02);  // evan data 071211	
	if (bFactoryMode!=TRUE){//user mode,open Dynamic OD
	    //WriteIIC560(0x4DB,0x52);
	    WriteIIC560(0x4DB,0x50);
	    //WriteIIC560(0x4DD,0x62);
	    WriteIIC560(0x4DD,0x52);// dynamic area is smaller than 1/16 -> 1/256 ratio, the whole frame is static.//dynamic area is Higher than 1/8 ratio, the whole frame is dynamic.
	}
	else{
	    WriteIIC560(0x4DB,0x50);
	    WriteIIC560(0x4DD,0x22);
	}
}
